// Primitive Data Types
// Number - Numeric values
let age = 25;
console.log('Type of age is', typeof age)

// String - Textual data enclosed in '' or ""
let address = "123, Sydney";

// Boolean - True or false
let isStudent = true;
let isInstructor = false;

// Null - intentional absence of value
let car = null;
console.log(car);

// Undefined - intentional absence of value
let city;
console.log('The type of city', typeof city);